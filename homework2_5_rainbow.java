public class homework2_5_rainbow{
    public static void main(String[] args){
        int r = 9;
        switch(r){
            case 1:
                System.out.println("Красный");
                break;
            case 2:
                System.out.println("Оранжевый");
                break;
            case 3:
                System.out.println("Желтый");
                break;
            case 4:
                System.out.println("Зеленый");
                break;
            case 5:
                System.out.println("Голубой");
                break;
            case 6:
                System.out.println("Синий");
                break;
            case 7:
                System.out.println("Фиолектовый");
                break;
            default:
                System.out.println("Нет такого номера цвета радуги");

        }
    }

}
